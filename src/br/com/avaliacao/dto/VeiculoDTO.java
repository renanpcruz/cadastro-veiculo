/**
 *
 * @author Renan
 */
public class VeiculoDTO {
    private String modelo_vei, cor_vei, fabricante_vei, placa_vei, proprietario_vei, motor_vei;
    private int ano_vei, nro_portas_vei, id_vei;

    public int getId_vei() {
        return id_vei;
    }

    public void setId_vei(int id_vei) {
        this.id_vei = id_vei;
    }

    public String getModelo_vei() {
        return modelo_vei;
    }

    public void setModelo_vei(String modelo_vei) {
        this.modelo_vei = modelo_vei;
    }

    public String getCor_vei() {
        return cor_vei;
    }

    public void setCor_vei(String cor_vei) {
        this.cor_vei = cor_vei;
    }

    public String getFabricante_vei() {
        return fabricante_vei;
    }

    public void setFabricante_vei(String fabricante_vei) {
        this.fabricante_vei = fabricante_vei;
    }

    public String getPlaca_vei() {
        return placa_vei;
    }

    public void setPlaca_vei(String placa_vei) {
        this.placa_vei = placa_vei;
    }

    public String getProprietario_vei() {
        return proprietario_vei;
    }

    public void setProprietario_vei(String proprietario_vei) {
        this.proprietario_vei = proprietario_vei;
    }

    public String getMotor_vei() {
        return motor_vei;
    }

    public void setMotor_vei(String motor_vei) {
        this.motor_vei = motor_vei;
    }

    public int getAno_vei() {
        return ano_vei;
    }

    public void setAno_vei(int ano_vei) {
        this.ano_vei = ano_vei;
    }

    public int getNro_portas_vei() {
        return nro_portas_vei;
    }

    public void setNro_portas_vei(int nro_portas_vei) {
        this.nro_portas_vei = nro_portas_vei;
    }
}
